  <h2 class="border_bot">EL RENUEVO DE DIOS</h2>
  <span class="data"><i>Por</i>: Alberto Lucas <i>Publicado:</i> 02 Febrero 2013</span>
  
  <p><i>Hageo 2: 9 La gloria postrera de esta casa será mayor que la primera, ha 
      dicho Jehová de los ejércitos; y daré paz en este lugar, dice Jehová de los ejércitos.
</i></p>

<p>Dios ha obrado permanentemente en la historia de la iglesia, conduciéndola hacia el cumplimiento de 
    Su propósito eterno: "<strong>Tener una gran familia de hijos e hijas semejantes a Jesús</strong>". Y aunque el hombre 
    creado a semejanza de Dios, hoy, sólo tiene vestigios de esa imagen, Jesús vino para regresarlo 
    a ella (Lucas 19:10). Él es la imagen del Dios invisible, el primogénito de toda creación. Juan 14:9; Colosenses 1:15.</p>

<cite>en la crónica de la iglesia quedaron tan deslumbrados con la parte de la iluminación que les tocó recibir, que al cuidarla, perdieron de vista Al que revela</cite>
<p>En esta dirección, es interesante observar que los diferentes movimientos en la crónica de la iglesia quedaron tan deslumbrados 
    con la parte de la iluminación que les tocó recibir, que al cuidarla, perdieron de vista Al que revela, edifica su iglesia y 
    la guía hacia la plenitud de Su proyecto. Éxodo 13:17-22; Números 9:15-23.. 
</p>
<p>Siendo así, y ya que el actual mover del Espíritu Santo forma parte de esa propia intervención de Dios en la vida de su pueblo, y 
    a fin de no repetir los anteriores desaciertos, haremos bien en considerar esta visita en dos aspectos. El primero es el contenido 
    mismo de la revelación recibida y el segundo (de similar valor) es que no nos aferremos tanto a esto como para perder la disposición 
    de ser dirigidos por el Señor a lo que sigue (Hageo 2:9; Juan 17:23; Efesios 4:13; Apocalipsis 19:7-9). Asumamos con humildad que no 
    estamos nosotros exentos de cometer errores similares a los que se incurrieron otrora.  </p>
<p>
    Convendrá también dejar establecido que renovar no es modernizar o actualizar, modificando, 
    ignorando, abandonando principios instaurados por Dios en su palabra imperecedera, 
    sino la completa reedificación del fundamento, conforme al original. 1ª Corintios 3:10-11..

</p>
